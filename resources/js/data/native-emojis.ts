export const getEmojisByCategory = (category: string): NativeEmoji[] => {
    return nativeEmojis.filter((emoji) => emoji.category === category);
};

export const searchEmojis = (query: string): NativeEmoji[] => {
    const lowercaseQuery = query.toLowerCase();
    return nativeEmojis.filter(
        (emoji) =>
            emoji.name.toLowerCase().includes(lowercaseQuery) ||
            emoji.keywords.some((keyword) => keyword.toLowerCase().includes(lowercaseQuery))
    );
};

export const getEmojiCategories = (): string[] => {
    return Array.from(new Set(nativeEmojis.map((emoji) => emoji.category)));
};
